    List<UsuarioRequisitanteModel> usuarios;
    public void listarUsuarios(int item){
        UsuarioRequisitanteDAO requi = new UsuarioRequisitanteDAO();
        usuarios = requi.listarUsuarios("%"+tf_pesquisar_usuario.getText()+"%",item);//tf_pesquisar_requisicao.getText().trim()
        mostraRequisicoes(usuarios);
    }
    public void mostraRequisicoes(List<UsuarioRequisitanteModel> requi){
        if(Jtable_usuarios.getRowCount() > 0){
            //Jtable_usuarios.removeRowSelectionInterval(0, Jtable_usuarios.getRowCount());
            DefaultTableModel modelo = (DefaultTableModel) Jtable_usuarios.getModel();
            modelo.setNumRows(0);
        }
        RequisicoesProdutoDAO d = new RequisicoesProdutoDAO();        
         if (requi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum usuário encontrado");

        } else {

            DefaultTableModel row = (DefaultTableModel) Jtable_usuarios.getModel();
            for (int i = 0; i < requi.size(); i++) {
                row.addRow(new Object[]{requi.get(i).getCodigo_requisitante(),requi.get(i).getNome_requisitante(),requi.get(i).getCpf()});
            }
        }
    }
    
    private void abrirEditar(){
        if(Jtable_usuarios.getSelectedRow() != -1){
            RequisitanteCadastroGUI abrir = new RequisitanteCadastroGUI();
            this.setEnabled(false);
            abrir.telaPai = this;
            abrir.codigo = Jtable_usuarios.getValueAt(Jtable_usuarios.getSelectedRow(), 0).toString();
            abrir.nome = Jtable_usuarios.getValueAt(Jtable_usuarios.getSelectedRow(), 1).toString();
            abrir.cpf = Jtable_usuarios.getValueAt(Jtable_usuarios.getSelectedRow(), 2).toString();
            abrir.update = true;
            abrir.codigoCarregar();
            abrir.setVisible(true);
        }
    }
    
    private void pesquisar(){
        if (jrb_codigo.isSelected() == true) {
            listarUsuarios(0);

        } else if (jrb_descricao.isSelected() == true) {
            listarUsuarios(1);
        } else if (jrb_detalhamento.isSelected() == true) {
            listarUsuarios(2);
        } else {
            listarUsuarios(0);
        }
    }
